.TH NCARGMV 8NCARG "JUNE 1991" NCAR "NCAR GRAPHICS"
.SH NAME
ncargmv \- Move an installed version of NCAR Graphics.
.SH SYNOPSIS
.B ncargmv
.PP
.SH DESCRIPTION
.B ncargmv
has no arguments and must be run interactively.  It is intended for use by a
system administrator, and will move an installed version of NCAR Graphics (at
revision level 3.1.2 or later) from its current position to a new position.
