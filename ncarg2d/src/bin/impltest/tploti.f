      SUBROUTINE TPLOTI
      COMMON /BLOCK1/ MESG,IDUMMY(500)
      CALL SET(0.,1.,0.,1.,0.,1.,0.,1.,1)
      CALL WTSTR(0.5,0.91,'DEMONSTRATION PLOT FOR PLOTIT',20,0,0)
      CALL PLOTIT (0,0,0)
      CALL PLOTIT (32767,0,1)
      CALL PLOTIT (32767,32767,1)
      CALL PLOTIT (0,32767,1)
      CALL PLOTIT (0,0,1)
      CALL PLOTIT (32767,32767,1)
      CALL PLOTIT (0,32767,0)
      CALL PLOTIT (32767,0,1)
      CALL FRAME
      WRITE(MESG,710)
  710 FORMAT(' TPLOTI EXITED--SEE PLOT TO VERIFY PERFORMANCE')
      RETURN
      END
