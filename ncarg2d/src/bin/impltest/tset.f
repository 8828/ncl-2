      SUBROUTINE TSET
      COMMON /BLOCK1/ MESG,IDUMMY(500)
      CALL SET(0.,1.,0.,1.,0.,1.,0.,1.,1)
      CALL WTSTR(0.5,0.95,'DEMONSTRATION PLOT FOR SET',25,0,0)
      DO 140 I=1,10
      UMIN = .05*(I-1)
      UMAX = 1.-UMIN
      CALL SET(UMIN,UMAX,UMIN,UMAX,0.,1.,0.,1.,1)
      CALL LINE(UMIN,UMIN,UMAX,UMIN)
      CALL LINE(UMAX,UMIN,UMAX,UMAX)
      CALL LINE(UMAX,UMAX,UMIN,UMAX)
      CALL LINE(UMIN,UMAX,UMIN,UMIN)
  140 CONTINUE
      CALL FRAME
      WRITE(MESG,770)
  770 FORMAT(' TSET   EXITED--SEE PLOT TO VERIFY PERFORMANCE')
      RETURN
      END
