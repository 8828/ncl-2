      SUBROUTINE MAPFST (XLAT,XLON)
      COMMON /MAPCMB/ IIER
      SAVE /MAPCMB/
      IF (ICFELL('MAPFST - UNCLEARED PRIOR ERROR',1).NE.0) THEN
        IIER=-1
        RETURN
      END IF
      CALL MAPIT (XLAT,XLON,0)
      IF (ICFELL('MAPFST',2).NE.0) RETURN
      RETURN
      END
