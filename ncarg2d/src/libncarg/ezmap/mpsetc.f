      SUBROUTINE MPSETC (WHCH,CVAL)
      CHARACTER*(*) WHCH,CVAL
      COMMON /MAPCMB/ IIER
      SAVE /MAPCMB/
      IF (ICFELL('MPSETC - UNCLEARED PRIOR ERROR',1).NE.0) THEN
        IIER=-1
        RETURN
      END IF
      CALL MAPSTC (WHCH,CVAL)
      IF (ICFELL('MPSETC',2).NE.0) RETURN
      RETURN
      END
