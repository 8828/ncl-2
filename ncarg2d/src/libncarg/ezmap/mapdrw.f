C                                         last call to MAPROJ.  The
C                                         default value is zero.
C
C                        PT          I,R  The value of PLAT from the
C                                         last call to MAPROJ.  The
C                                         default value is zero.
C
C                        Pn          I,R  "n" is an integer between 1
C                                         and 8.  Retrieves values from
