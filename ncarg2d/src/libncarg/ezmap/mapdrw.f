C                        or if the error flag 'ER' is set, MAPLBL does
C                        nothing.
C
C USAGE                  CALL MAPLBL
C
C ARGUMENTS              None.
C
C-----------------------------------------------------------------------
C S U B R O U T I N E   M A P L O T   -   D E S C R I P T I O N
C-----------------------------------------------------------------------
C
C PURPOSE                To draw the continental and/or international
