      SUBROUTINE MPSETR (WHCH,RVAL)
      CHARACTER*(*) WHCH
      COMMON /MAPCMB/ IIER
      SAVE /MAPCMB/
      IF (ICFELL('MPSETR - UNCLEARED PRIOR ERROR',1).NE.0) THEN
        IIER=-1
        RETURN
      END IF
      CALL MAPSTR (WHCH,RVAL)
      IF (ICFELL('MPSETR',2).NE.0) RETURN
      RETURN
      END
