C
      SUBROUTINE MPTRUI (UVAL,VVAL,RLAT,RLON)
C
C Declare common blocks required to communicate with USGS code.
C
        COMMON /ERRMZ0/ IERR
        SAVE   /ERRMZ0/
C
        COMMON /PROJZ0/ IPRO
        SAVE   /PROJZ0/
C
        COMMON /USGSC1/ IPRF,UTPA(15),UUMN,UUMX,UVMN,UVMX
          DOUBLE PRECISION UTPA
        SAVE   /USGSC1/
C
C Declare required double-precision coordinate arrays.
C
        DOUBLE PRECISION CRDI(2),CRDO(2),CRDT(2)
C
C Define a couple of conversion constants.
C
        DOUBLE PRECISION DTOR,RTOD
C
        DATA DTOR / .017453292519943D0 /
        DATA RTOD / 57.2957795130823D0 /
C
C Given UVAL and VVAL, in meters, return RLAT and RLON, in degrees.
C
        CRDI(1)=DBLE(UVAL)
        CRDI(2)=DBLE(VVAL)
C
        GO TO (101,102,103,104,105,106,107,108,109,110,
     +         111,112,113,114,115,116,117,118,119,120,
     +         121,122,123                             ) , IPRO
C
  101   CALL PJ01Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ01Z0 (CRDO,CRDT,0)
        GO TO 201
C
  102   CALL PJ02Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ02Z0 (CRDO,CRDT,0)
        GO TO 201
C
  103   CALL PJ03Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ03Z0 (CRDO,CRDT,0)
        GO TO 201
C
  104   CALL PJ04Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ04Z0 (CRDO,CRDT,0)
        GO TO 201
C
  105   CALL PJ05Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ05Z0 (CRDO,CRDT,0)
        GO TO 201
C
  106   CALL PJ06Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ06Z0 (CRDO,CRDT,0)
        GO TO 201
C
  107   CALL PJ07Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ07Z0 (CRDO,CRDT,0)
        GO TO 201
C
  108   CALL PJ08Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ08Z0 (CRDO,CRDT,0)
        GO TO 201
C
  109   CALL PJ09Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ09Z0 (CRDO,CRDT,0)
        GO TO 201
C
  110   CALL PJ10Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ10Z0 (CRDO,CRDT,0)
        GO TO 201
C
  111   CALL PJ11Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ11Z0 (CRDO,CRDT,0)
        GO TO 201
C
  112   CALL PJ12Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ12Z0 (CRDO,CRDT,0)
        GO TO 201
C
  113   CALL PJ13Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ13Z0 (CRDO,CRDT,0)
        GO TO 201
C
  114   CALL PJ14Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ14Z0 (CRDO,CRDT,0)
        GO TO 201
C
  115   CALL PJ15Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ15Z0 (CRDO,CRDT,0)
        GO TO 201
C
  116   CALL PJ16Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ16Z0 (CRDO,CRDT,0)
        GO TO 201
C
  117   CALL PJ17Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ17Z0 (CRDO,CRDT,0)
        GO TO 201
C
  118   CALL PJ18Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ18Z0 (CRDO,CRDT,0)
        GO TO 201
C
  119   CALL PJ19Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ19Z0 (CRDO,CRDT,0)
        GO TO 201
C
  120   CALL PJ20Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ20Z0 (CRDO,CRDT,0)
        GO TO 201
C
  121   IF (CRDI(1).GE.-3.096982037908766D0*UTPA(1).AND.
     +      CRDI(1).LE.+3.096982037908766D0*UTPA(1).AND.
     +      CRDI(2).GE.-1.570796326793281D0*UTPA(1).AND.
     +      CRDI(2).LE.+1.570796326793281D0*UTPA(1)) THEN
          CALL PJ21Z0 (CRDI,CRDO,1)
          IF (IERR.EQ.0) CALL PJ21Z0 (CRDO,CRDT,0)
        ELSE
          IERR=1
        END IF
        GO TO 201
C
  122   CALL PJ22Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ22Z0 (CRDO,CRDT,0)
        GO TO 201
C
  123   CALL PJ23Z0 (CRDI,CRDO,1)
        IF (IERR.EQ.0) CALL PJ23Z0 (CRDO,CRDT,0)
        GO TO 201
C
  201   IF (IERR.EQ.0) THEN
          IF (ABS(CRDT(1)-CRDI(1)).LT.1.D-5*DBLE(UUMX-UUMN).AND.
     +        ABS(CRDT(2)-CRDI(2)).LT.1.D-5*DBLE(UVMX-UVMN)) THEN
            RLAT=REAL(RTOD*CRDO(2))
            RLON=REAL(RTOD*CRDO(1))
          ELSE
            RLAT=1.E12
            RLON=1.E12
            IERR=1
          END IF
        ELSE
          RLAT=1.E12
          RLON=1.E12
          IERR=2
        END IF
C
        RETURN
C
      END
