.TH FCAPS 1NCAR "JUNE 1990"
.SH NAME
fcaps \- 
report available fontcaps
.SH SYNOPSIS
.B fcaps
[
.B \-V
]
.SH DESCRIPTION
.B fcaps
is a simple script that reports all the available fontcaps for use in 
metafile translation.
.SH OPTIONS
.TP
.B \-V
Print the version number and then exit.
